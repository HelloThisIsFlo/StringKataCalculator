public class NumberStringFormatException extends Exception {

    public NumberStringFormatException() {
    }

    public NumberStringFormatException(String message) {
        super(message);
    }

    public NumberStringFormatException(String message, Throwable cause) {
        super(message, cause);
    }

    public NumberStringFormatException(Throwable cause) {
        super(cause);
    }

    public NumberStringFormatException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
